int     ft_strcmp(unsigned char *s1,unsigned char *s2)
{
    int i;
    int j;
    
    i = 0;
    j = 0;
    while (s1[i] != '\0' || s2[i] != '\0')
    {
        if (s1[i] != s2[i])
        return (s1[i] - s2[i]);
        i++;
    }
    return (0);
}