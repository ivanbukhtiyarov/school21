#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	i;
	char	*str;

	str = (char*)malloc(ft_strlen(s1) + 1);
	if (!str)
	{
		return (NULL);
	}
	i = 0;

	while (i < ft_strlen(s1))
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
